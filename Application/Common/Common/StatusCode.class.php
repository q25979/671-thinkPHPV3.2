// +--------------------------------------------------------
// | 0 请求成功
// +--------------------------------------------------------
// | - 表示认证失败
// +--------------------------------------------------------
// | 1000~1999 表示用户账号类出错
// +--------------------------------------------------------
// | 2000~2999 表示权限错误
// +--------------------------------------------------------
// | 3000~3999 空数据
// +--------------------------------------------------------
// | 4000~4999 操作数据库错误
// +--------------------------------------------------------
// | 5000~5999 关于文章
// +--------------------------------------------------------
// | 6000~6999 关于商品
// +--------------------------------------------------------
// | 7000~7999 关于文件
// +--------------------------------------------------------
// | 8000~8999
// +--------------------------------------------------------
// | 9000~9999
// +--------------------------------------------------------

class StatusCode {
    // 定义状态码
    static private $code = array(
        0       =>  "数据请求成功",

        -100    =>  "签名验证错误",
        -101    =>  "身份验证失败",

        1000    =>  "用户名不能为空",
        1001    =>  "用户名已存在",
        1002    =>  "用户名不存在",
        1003    =>  "密码不能为空",
        1004    =>  "密码错误",
        1005    =>  "账号添加错误",
        1006    =>  "验证码错误",
        1007    =>  "您的账号已被封",
        1008    =>  "手机号码格式错误",
        1009    =>  "系统名不能为空",
        1010    =>  "该系统名已存在！",
        1011    =>  "该系统名已被注册！",

        2000    =>  "越权操作",
        2001    =>  "授权失败",

        3000    =>  "接收的省份为空数据",
        3001    =>  "接收的城市为空数据",
        3002    =>  "接收的区域为空数据",
        3003    =>  "接接收的id为空",

        4000    =>  "数据库更新数据失败",
        4001    =>  "数据插入失败",
        4002    =>  "数据查询失败",
        4003    =>  "数据删除失败",

        5000    =>  "该文章不存在,无法生成链接",

        6000    =>  "该商品已收藏",

        7000    =>  "文件上传服务器失败",
        7001    =>  "文件类型错误",
    );

    /**
     * 生成状态码与相应的描述
     * @param $code     状态码
     * @return mixed    返回状态码与相关信息
     */
    public function code($code) {
        $data['code'] = $code;
        $data['msg'] = self::$code[$code];

        return $data;
    }
}